function recommendMovies() {
    const genre = document.getElementById("genre").value;
    const language = document.getElementById("language").value;
    const movieList = document.getElementById("movies");
    const modal = document.getElementById("popupModal");

    if (genre && language) {
        const movies = movieData[genre][language];

        movieList.innerHTML =
            `<p><strong>Recommended ${language} ${genre.toLowerCase()} movies:</strong></p>`;

        movieList.innerHTML +=
            `<div class="movie-grid">${movies.map(movie =>
                `<div class="movie-card">
                    <a href="${movie.trailer}" target="_blank">
                        <img src="${movie.poster}" alt="${movie.title} Poster">
                    </a>
                    <div class="movie-title">${movie.title}</div>
                </div>`
            ).join('')}</div>`;
    } else {
        movieList.innerHTML = `<p>Please select both genre and language.</p>`;
    }

    modal.classList.add("show");
}

function refreshPage() {
    document.getElementById("genre").value = "";
    document.getElementById("language").value = "";
    document.getElementById("movies").innerHTML = "";
    closeModal();
}

function closeModal() {
    document.getElementById("popupModal").classList.remove("show");
}

window.onclick = function(event) {
    const modal = document.getElementById("popupModal");
    if (event.target === modal) {
        closeModal();
    }
};